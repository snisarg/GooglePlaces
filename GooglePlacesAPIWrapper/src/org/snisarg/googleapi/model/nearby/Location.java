
package org.snisarg.googleapi.model.nearby;

public class Location{
   	private double lat;
   	private double lng;

 	public Number getLat(){
		return this.lat;
	}
	public void setLat(double lat){
		this.lat = lat;
	}
 	public double getLng(){
		return this.lng;
	}
	public void setLng(double lng){
		this.lng = lng;
	}
}
